// home /////////////////////////////////////////////////////////////////////////////////////////
export const HomeRoute = (id?: string): string => `/${id ? `?id=${id}` : ''}`;

// account /////////////////////////////////////////////////////////////////////////////////////////
export const RegisterRoute = (): string => `/register`;
export const LoginRoute = (): string => `/login`;

// grade /////////////////////////////////////////////////////////////////////////////////////////
export const GradeRoute = (gradeId: string | number, tabName: string): string =>
  `/${gradeId}/${tabName}/`;
